async function generateVideoThumbnail(file) {
    return new Promise((resolve, reject) => {
        if (!file.type.includes('video')) reject(new Error('Not a video'));
        const video = document.createElement('video');
        video.preload = 'metadata';
        const timeout = setTimeout(() => { URL.revokeObjectURL(video.src); reject(new Error('Timeout')); }, 10000);
        video.onloadeddata = () => { clearTimeout(timeout); video.currentTime = Math.min(1, video.duration * 0.1); };
        video.onseeked = () => {
            const canvas = document.createElement('canvas');
            canvas.width = video.videoWidth;
            canvas.height = video.videoHeight;
            canvas.getContext('2d').drawImage(video, 0, 0, canvas.width, canvas.height);
            canvas.toBlob(blob => {
                URL.revokeObjectURL(video.src);
                resolve(new File([blob], 'thumbnail.jpg', { type: 'image/jpeg', lastModified: Date.now() }));
            }, 'image/jpeg', 0.9);
        };
        video.onerror = () => { clearTimeout(timeout); URL.revokeObjectURL(video.src); reject(new Error('Failed to load')); };
        video.src = URL.createObjectURL(file);
    });
}

function handleThumbnailFile(file, isAutoGenerated = false) {
    const imageTypes = ['image/jpeg', 'image/jpg', 'image/png', 'image/gif', 'image/webp'];
    if (!imageTypes.includes(file.type)) { showToast('Please select a valid image file'); return; }
    
    selectedThumbnailFile = file;
    const reader = new FileReader();
    reader.onload = function(e) {
        document.getElementById('thumbnail-image').src = e.target.result;
        document.getElementById('thumbnail-preview').style.display = 'block';
    };
    reader.readAsDataURL(file);
    
    if (!isAutoGenerated) showToast('Thumbnail image selected', 'success');
    updateChecklist();
    saveDraftToLocalStorage();
}

function setupThumbnailUpload() {
    const zone = document.getElementById('thumbnail-upload-zone');
    const input = document.getElementById('thumbnail-file-input');
    zone.addEventListener('click', () => input.click());
    input.addEventListener('change', (e) => { if (e.target.files.length) handleThumbnailFile(e.target.files[0], false); });
    document.getElementById('camera-file-input').addEventListener('change', (e) => { if (e.target.files.length) handleThumbnailFile(e.target.files[0], false); });
}

async function uploadThumbnailOnly(file) {
    // Media on this platform is never stored in Supabase Storage — the
    // get-upload-url edge function routes video to Cloudflare Stream and
    // everything else (images included) to Cloudflare R2 via a pre-signed
    // URL. This now matches the same handshake+XHR path processCreatorPublish
    // already uses for a video's own custom thumbnail.
    try {
        const handshake = await BANTU_UPLOAD_ENGINE.getSecureUploadHandshake('image', file.name);
        await BANTU_UPLOAD_ENGINE.uploadFileViaXHR(file, handshake.uploadUrl, false, () => {});
        return handshake.fileUrl;
    } catch (error) {
        console.error('Thumbnail upload error:', error);
        return null;
    }
}
